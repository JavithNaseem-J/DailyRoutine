class _WaterWalkRow extends StatelessWidget {
  const _WaterWalkRow({
    required this.waterMl,
    required this.waterTarget,
    required this.walkM,
    required this.walkTarget,
    required this.onWaterTap,
    required this.onWalkTap,
  });

  final int waterMl;
  final int waterTarget;
  final int walkM;
  final int walkTarget;
  final ValueChanged<int> onWaterTap;
  final ValueChanged<int> onWalkTap;

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        Expanded(
          child: _GoalRing(
            icon: Icons.water_drop_rounded,
            label: 'Water',
            ringColor: Color(0xFF3B82F6),
            fraction: waterMl / waterTarget,
            valueText: '${(waterMl / 1000).toStringAsFixed(1)} L',
            onIncrement: () => onWaterTap(250),
            onDecrement: () => onWaterTap(-250),
          ),
        ),
        const SizedBox(width: 12),
        Expanded(
          child: _GoalRing(
            icon: Icons.directions_walk_rounded,
            label: 'Walk',
            ringColor: AppColors.complete,
            fraction: walkM / walkTarget,
            valueText: '${(walkM / 1000).toStringAsFixed(1)} km',
            onIncrement: () => onWalkTap(500),
            onDecrement: () => onWalkTap(-500),
          ),
        ),
      ],
    );
  }
}

class _GoalRing extends StatelessWidget {
  const _GoalRing({
    required this.icon,
    required this.label,
    required this.ringColor,
    required this.fraction,
    required this.valueText,
    required this.onIncrement,
    required this.onDecrement,
  });

  final IconData icon;
  final String label;
  final Color ringColor;
  final double fraction;
  final String valueText;
  final VoidCallback onIncrement;
  final VoidCallback onDecrement;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.symmetric(vertical: 16, horizontal: 14),
      decoration: BoxDecoration(
        color: AppColors.cardSurface,
        borderRadius: BorderRadius.circular(14),
      ),
      child: Column(
        children: [
          Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              Icon(icon, color: ringColor, size: 14),
              const SizedBox(width: 5),
              Text(
                label,
                style: AppTypography.body(
                  size: 12,
                  weight: FontWeight.w600,
                  color: AppColors.textPrimary,
                ),
              ),
            ],
          ),
          SizedBox(height: 14),
          TweenAnimationBuilder<double>(
            tween: Tween(begin: 0, end: fraction.clamp(0.0, 1.0)),
            duration: const Duration(milliseconds: 600),
            curve: Curves.easeOut,
            builder: (_, value, __) => SizedBox(
              width: 90,
              height: 90,
              child: CustomPaint(
                painter: _ArcPainter(
                  fraction: value,
                  ringColor: ringColor,
                  trackColor: AppColors.surfaceRaised,
                  strokeWidth: 9,
                ),
                child: Center(
                  child: AnimatedSwitcher(
                    duration: const Duration(milliseconds: 300),
                    child: value >= 1.0
                        ? Icon(
                            Icons.check_circle_rounded,
                            color: ringColor,
                            size: 32,
                          )
                        : Text(
                            valueText,
                            textAlign: TextAlign.center,
                            style: AppTypography.mono(
                              size: 12,
                              weight: FontWeight.w700,
                              color: AppColors.textPrimary,
                            ),
                          ),
                  ),
                ),
              ),
            ),
          ),
          SizedBox(height: 14),
          Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              _GoalBtn(icon: Icons.remove, onTap: onDecrement),
              const SizedBox(width: 22),
              _GoalBtn(
                icon: Icons.add,
                onTap: onIncrement,
                filled: true,
                color: ringColor,
              ),
            ],
          ),
        ],
      ),
    );
  }
}

