class _ProgressCard extends StatefulWidget {
  const _ProgressCard({
    super.key,
    required this.item,
    required this.index,
    required this.width,
    required this.bgColor,
    required this.accent,
    required this.cardIcon,
    required this.onNameChanged,
    required this.onProgressChanged,
    required this.onNoteChanged,
    required this.onDelete,
  });

  final ProgressItem item;
  final int index;
  final double width;
  final Color bgColor;
  final Color accent;
  final IconData cardIcon;
  final ValueChanged<String> onNameChanged;
  final ValueChanged<int> onProgressChanged;
  final ValueChanged<String> onNoteChanged;
  final VoidCallback onDelete;

  @override
  State<_ProgressCard> createState() => _ProgressCardState();
}

class _ProgressCardState extends State<_ProgressCard> {
  late final TextEditingController _nameCtrl;
  late final TextEditingController _noteCtrl;
  bool _isEditing = false;

  @override
  void initState() {
    super.initState();
    _nameCtrl = TextEditingController(text: widget.item.name);
    _noteCtrl = TextEditingController(text: widget.item.note);
  }

  @override
  void dispose() {
    _nameCtrl.dispose();
    _noteCtrl.dispose();
    super.dispose();
  }

  void _save() {
    widget.onNameChanged(_nameCtrl.text);
    widget.onNoteChanged(_noteCtrl.text);
    setState(() => _isEditing = false);
  }

  void _showEditOptions(BuildContext context) {
    showModalBottomSheet(
      context: context,
      useRootNavigator: true,
      backgroundColor: AppColors.background,
      shape: const RoundedRectangleBorder(
        borderRadius: BorderRadius.vertical(top: Radius.circular(20)),
      ),
      builder: (ctx) => Container(
        padding: EdgeInsets.only(
          bottom: MediaQuery.of(ctx).padding.bottom + kBottomNavigationBarHeight,
        ),
        child: SafeArea(
          bottom: false, // We handle bottom padding manually above
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              const SizedBox(height: 8),
              Center(
                child: Container(
                  width: 40,
                  height: 4,
                  decoration: BoxDecoration(
                    color: AppColors.surfaceRaised,
                    borderRadius: BorderRadius.circular(2),
                  ),
                ),
              ),
              const SizedBox(height: 16),
              InkWell(
                onTap: () {
                  Navigator.pop(ctx);
                  setState(() => _isEditing = true);
                },
                child: Container(
                  height: 56,
                  alignment: Alignment.center,
                  child: Row(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      Icon(Icons.edit_rounded, color: AppColors.textPrimary, size: 20),
                      const SizedBox(width: 12),
                      Text('Edit', style: AppTypography.body(size: 16)),
                    ],
                  ),
                ),
              ),
              InkWell(
                onTap: () {
                  Navigator.pop(ctx);
                  widget.onDelete();
                },
                child: Container(
                  height: 56,
                  alignment: Alignment.center,
                  child: Row(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      Icon(Icons.delete_outline_rounded, color: AppColors.moodLow, size: 20),
                      const SizedBox(width: 12),
                      Text('Delete', style: AppTypography.body(size: 16, color: AppColors.moodLow)),
                    ],
                  ),
                ),
              ),
              InkWell(
                onTap: () => Navigator.pop(ctx),
                child: Container(
                  height: 56,
                  alignment: Alignment.center,
                  child: Row(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      Icon(Icons.close_rounded, color: AppColors.textPrimary, size: 20),
                      const SizedBox(width: 12),
                      Text('Cancel', style: AppTypography.body(size: 16)),
                    ],
                  ),
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    final pct = widget.item.progress;
    final isComplete = pct >= 100;

    return GestureDetector(
      onLongPress: () => _showEditOptions(context),
      child: Container(
        width: widget.width,
        margin: const EdgeInsets.only(right: 14),
        decoration: BoxDecoration(
          borderRadius: BorderRadius.circular(20),
          boxShadow: [
            BoxShadow(
              color: Colors.black.withValues(alpha: 0.05),
              blurRadius: 10,
              offset: const Offset(0, 4),
            ),
          ],
        ),
        child: ClipRRect(
          borderRadius: BorderRadius.circular(20),
          child: Container(
            color: Colors.white,
            child: Stack(
              children: [
                // BLACK BORDER OVERLAY
                Positioned.fill(
                  child: Container(
                    decoration: BoxDecoration(
                      borderRadius: BorderRadius.circular(20),
                      border: Border.all(color: Colors.black, width: 2.5),
                    ),
                  ),
                ),

                // MAIN CONTENT COLUMN
                Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Expanded(
                      child: Padding(
                        padding: const EdgeInsets.fromLTRB(18, 16, 18, 0),
                        child: _isEditing
                            // â”€â”€ EDIT MODE â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
                            ? Column(
                                crossAxisAlignment: CrossAxisAlignment.start,
                                children: [
                                  Row(
                                    children: [
                                      Expanded(
                                        child: TextField(
                                          controller: _noteCtrl,
                                          autofocus: true,
                                          style: AppTypography.body(
                                            size: 16,
                                            weight: FontWeight.w700,
                                            color: Colors.black,
                                          ),
                                          decoration: InputDecoration(
                                            hintText: 'Task Name',
                                            hintStyle: AppTypography.body(
                                              size: 16,
                                              color: Colors.black38,
                                            ),
                                            border: InputBorder.none,
                                            enabledBorder: InputBorder.none,
                                            focusedBorder: InputBorder.none,
                                            filled: false,
                                            isDense: true,
                                            contentPadding: EdgeInsets.zero,
                                          ),
                                        ),
                                      ),
                                      GestureDetector(
                                        onTap: _save,
                                        child: Container(
                                          padding: const EdgeInsets.all(7),
                                          decoration: BoxDecoration(
                                            color: Colors.black,
                                            shape: BoxShape.circle,
                                          ),
                                          child: Icon(
                                            Icons.check_rounded,
                                            size: 16,
                                            color: Colors.white,
                                          ),
                                        ),
                                      ),
                                    ],
                                  ),
                                  SizedBox(height: 8),
                                  Expanded(
                                    child: TextField(
                                      controller: _nameCtrl,
                                      maxLines: null,
                                      expands: true,
                                      style: AppTypography.body(
                                        size: 13,
                                        weight: FontWeight.w500,
                                        color: Colors.black87,
                                        height: 1.35,
                                      ),
                                      decoration: InputDecoration(
                                        hintText: 'Description',
                                        hintStyle: AppTypography.body(
                                          size: 13,
                                          color: Colors.black38,
                                        ),
                                        border: InputBorder.none,
                                        enabledBorder: InputBorder.none,
                                        focusedBorder: InputBorder.none,
                                        filled: false,
                                        isDense: true,
                                        contentPadding: EdgeInsets.zero,
                                      ),
                                    ),
                                  ),
                                ],
                              )
                            // â”€â”€ READ-ONLY (TOOLTIP) MODE â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
                            : Column(
                                crossAxisAlignment: CrossAxisAlignment.start,
                                children: [
                                  Row(
                                    crossAxisAlignment:
                                        CrossAxisAlignment.center,
                                    children: [
                                      Expanded(
                                        child: Text(
                                          _noteCtrl.text.isEmpty
                                              ? 'Task Name'
                                              : _noteCtrl.text,
                                          maxLines: 1,
                                          overflow: TextOverflow.ellipsis,
                                          style: AppTypography.body(
                                            size: 16,
                                            weight: FontWeight.w700,
                                            color: _noteCtrl.text.isEmpty
                                                ? Colors.black38
                                                : Colors.black,
                                          ),
                                        ),
                                      ),
                                      const SizedBox(width: 10),
                                      Text(
                                        isComplete ? 'DONE' : '$pct%',
                                        style: AppTypography.mono(
                                          size: 16,
                                          weight: FontWeight.w900,
                                          color: Colors.black,
                                        ),
                                      ),
                                    ],
                                  ),
                                  SizedBox(height: 8),
                                  Text(
                                    _nameCtrl.text.isEmpty
                                        ? 'Description'
                                        : _nameCtrl.text,
                                    maxLines: 2,
                                    overflow: TextOverflow.ellipsis,
                                    style: AppTypography.body(
                                      size: 13,
                                      weight: FontWeight.w500,
                                      color: _nameCtrl.text.isEmpty
                                          ? Colors.black26
                                          : Colors.black54,
                                      height: 1.35,
                                    ),
                                  ),
                                ],
                              ),
                      ),
                    ),

                    // PROGRESS BAR â€” drag/tap zone at the bottom of the column
                    GestureDetector(
                      behavior: HitTestBehavior.opaque,
                      onPanUpdate: (d) {
                        final p = (d.localPosition.dx / widget.width * 100)
                            .clamp(0.0, 100.0)
                            .round();
                        widget.onProgressChanged(p);
                      },
                      onTapDown: (d) {
                        final p = (d.localPosition.dx / widget.width * 100)
                            .clamp(0.0, 100.0)
                            .round();
                        widget.onProgressChanged(p);
                      },
                      child: Padding(
                        padding: const EdgeInsets.fromLTRB(18, 6, 18, 14),
                        child: Stack(
                          children: [
                            // Track
                            Container(
                              height: 8,
                                width: double.infinity,
                                decoration: BoxDecoration(
                                  color: Color(0xFFE5E7EB),
                                  borderRadius: BorderRadius.circular(4),
                                ),
                              ),
                              // Fill
                              AnimatedContainer(
                                duration: const Duration(milliseconds: 120),
                                curve: Curves.easeOut,
                                height: 8,
                                width: (widget.width - 36) * (pct / 100).clamp(0.0, 1.0),
                                decoration: BoxDecoration(
                                  color: AppColors.primary,
                                  borderRadius: BorderRadius.circular(4),
                              ),
                            ),
                          ],
                        ),
                      ),
                    ),
                  ],
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}

